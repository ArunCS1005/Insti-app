class Meal {
  final int year;
  final int month;
  final int day;
  final String type;
  final String time;
  final String dailyItem;
  final String regulars;
  final String vegspecials;
  final String nonvegspecials;

  Meal({
    required this.year,
    required this.month,
    required this.day,
    required this.type,
    required this.time,
    required this.dailyItem,
    required this.regulars,
    required this.vegspecials,
    required this.nonvegspecials,
  });
}
